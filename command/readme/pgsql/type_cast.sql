select cast(123 as varchar),            -- 数字转字符串
       123 :: varchar,                  -- 数字强转字符串
       cast('000123' as int),           -- 字符串转数字
       '000123' :: int;                 -- 字符串强转数字

select to_char(123456, 'fm00000000'),   -- fm00...，待转化值位数少于格式位数，补0
       to_char(123456, 'fm0000'),       -- fm00...，待转化值位数多于格式位数，输出：####
       to_char(123456, 'fm99999999'),   -- fm99...，待转化值位数少于格式位数，不变
       to_char(123456, 'fm9999');       -- fm99...，待转化值位数多于格式位数，输出：####
